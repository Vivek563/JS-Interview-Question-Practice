// what are the diffrences between cookies, sessions and local storage?

// Cookies:
// 1. Cookies are used to store information on a client’s computer.
// 2. Cookies are plain text data record of 5 variable-length fields −
//     a. Expires − The date the cookie will expire. If this is blank, the cookie will expire when the visitor quits the browser.
//     b. Domain − The domain name of your site.
//     c. Path − The path to the directory or web page that set the cookie. This may be blank if you want to retrieve the cookie from any directory or page.
//     d. Secure − If this field contains the word "secure", then the cookie may only be retrieved with a secure server. If this field is blank, no such restriction exists.
//     e. Name=Value − Cookies are set and retrieved in the form of key-value pairs.
// 3. Cookies are used to remember information about the user profile(such as username).


// Sessions:
// 1. Sessions are used to store information on a server.
// 2. Sessions are stored on server side.
// 3. Sessions are used to remember information about the user profile(such as username).


// Local Storage:
// 1. Local Storage is used to store information on a client’s computer.
// 2. Local Storage is stored on client side.
// 3. Local Storage is used to remember information about the user profile(such as username).
// 4. Local Storage is not sent with every request, so it doesn’t worsen performance (especially for mobile data connections).



